// Incremento (++) e Decremento (--)
++x
x++
const a = ++2 // já recebe o incrementado 3
const a = 2++ // recebe o valor atual e depois é incrementado 3

--x
x--
const a = --2 // já recebe o decrementado 1
const a = 2-- // valor atual e depois decrementa 1

